package org.broadband_forum.obbaa.netconf.mn.fwk.server.model.util;

public class EditTreeTransformerException extends Exception {

    private static final long serialVersionUID = 1L;

    public EditTreeTransformerException() {
        super();
    }

    public EditTreeTransformerException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public EditTreeTransformerException(String message, Throwable cause) {
        super(message, cause);
    }

    public EditTreeTransformerException(String message) {
        super(message);
    }

    public EditTreeTransformerException(Throwable cause) {
        super(cause);
    }

}
